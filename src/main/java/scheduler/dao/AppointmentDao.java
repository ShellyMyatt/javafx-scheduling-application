    /**
     * This method inserts an appointment into the MySQL database.
     *
     * @param appointment the appointment object containing details to be inserted into the database.
     * @return true if the appointment is successfully inserted. False if not.
     * @throws SQLException if an error occurs while inserting an appointment.
     */
    public boolean addAppointment(Appointment appointment) throws SQLException {

        String insertAppointment = "INSERT INTO appointments(Title, Description, Location, Contact_ID, Type, Start, End, Customer_ID, User_ID) VALUES (?,?,?,?,?,?,?,?,?)";
        try (PreparedStatement insertSqlStatement = DBConnection.getConnection().prepareStatement(insertAppointment, PreparedStatement.RETURN_GENERATED_KEYS)) {
            insertSqlStatement.setString(1, appointment.getTitle());
            insertSqlStatement.setString(2, appointment.getDescription());
            insertSqlStatement.setString(3, appointment.getLocation());
            insertSqlStatement.setInt(4, appointment.getContacts().getContactID());
            insertSqlStatement.setString(5, appointment.getType());
            insertSqlStatement.setTimestamp(6, Timestamp.valueOf(appointment.getStart()));
            insertSqlStatement.setTimestamp(7, Timestamp.valueOf(appointment.getEnd()));
            insertSqlStatement.setInt(8, appointment.getCustomerID());
            insertSqlStatement.setInt(9, appointment.getUserID());

            int appointmentInserted = insertSqlStatement.executeUpdate();

            if (appointmentInserted == 1) {
                try (ResultSet autoGeneratedID = insertSqlStatement.getGeneratedKeys()) {
                    if (autoGeneratedID.next()) {
                        int appointmentID = autoGeneratedID.getInt(1);
                        appointment.setAppointmentID(appointmentID);
                    }
                }
                return true;
            }
        } catch (SQLException e) {
            System.err.println("Error inserting appointment" + e.getMessage());
            return false;
        }
        return false;
    }

    /**
     * This Method retrieves all the appointments from the MySQL database.
     * Appointments are displayed in the user's system default time zone.
     *
     * @return an ObservableList containing all the appointments in the database.
     * @throws SQLException if an error occurs while retrieving the appointments.
     */
    public static ObservableList<Appointment> getAllAppointments() throws SQLException {
        ObservableList<Appointment> appointmentObservableList = FXCollections.observableArrayList();
        System.out.println("Retrieving all appointments");

        String sql = """
        SELECT a.Appointment_ID, a.Title, a.Description, a.Location, a.Type, a.Start, a.End,
               a.Customer_ID, a.User_ID, c.Contact_ID, c.Contact_Name, c.Email
        FROM appointments AS a
        JOIN contacts AS c ON a.Contact_ID = c.Contact_ID
        """;
        try (PreparedStatement statement = DBConnection.getConnection().prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery()) {

            while (resultSet.next()) {

                int appointmentID = resultSet.getInt("Appointment_ID");
                String title = resultSet.getString("Title");
                String description = resultSet.getString("Description");
                String location = resultSet.getString("Location");
                String type = resultSet.getString("Type");

                //Get UTC times from the database
                LocalDateTime start = resultSet.getTimestamp("Start").toLocalDateTime();
                LocalDateTime end = resultSet.getTimestamp("End").toLocalDateTime();

                //Converts UTC times to local time
//                LocalDateTime startLocal = TimeConversions.toLocalTime(startUTC);
//                LocalDateTime endLocal = TimeConversions.toLocalTime(endUTC);

                int customerID = resultSet.getInt("Customer_ID");
                int userID = resultSet.getInt("User_ID");

                int contactID = resultSet.getInt("Contact_ID");
                String contactName = resultSet.getString("Contact_Name");
                String email = resultSet.getString("Email");

                Contacts contacts = new Contacts(contactID, contactName, email);

                Appointment appointment = new Appointment(appointmentID, title, description, location, contacts, type, start, end, customerID, userID);

                appointmentObservableList.add(appointment);
            }
        } catch (SQLException e) {
            System.err.println("ERROR getting all appointments" + e.getMessage());
            throw e;
        }
        return appointmentObservableList;
    }

    /**
     * This method updates an appointment in the MySQL database.
     *
     * @param appointment the appointment object containing updated details to be saved to the database.
     * @return true if the appointment is successfully updated. False if not.
     * @throws SQLException if a database error occurs while updating the appointment.
     */
    public boolean update(Appointment appointment) throws SQLException {
        boolean updated;

        String updateAppointment = "UPDATE appointments SET Title = ?, Description = ?, Location = ?,Contact_ID = ?, Type = ?, Start = ?, End = ?, Customer_ID = ?, User_ID = ? WHERE Appointment_ID = ?";
        try(PreparedStatement updateSqlStatement = DBConnection.getConnection().prepareStatement(updateAppointment)) {

            updateSqlStatement.setString(1, appointment.getTitle());
            updateSqlStatement.setString(2, appointment.getDescription());
            updateSqlStatement.setString(3, appointment.getLocation());
            updateSqlStatement.setInt(4, appointment.getContacts().getContactID());
            updateSqlStatement.setString(5, appointment.getType());
            updateSqlStatement.setTimestamp(6, Timestamp.valueOf(appointment.getStart()));
            updateSqlStatement.setTimestamp(7, Timestamp.valueOf(appointment.getEnd()));
            updateSqlStatement.setInt(8, appointment.getCustomerID());
            updateSqlStatement.setInt(9, appointment.getUserID());
            updateSqlStatement.setInt(10, appointment.getAppointmentID());

            updateSqlStatement.executeUpdate();
            updated = true;

        } catch (SQLException e) {
        System.err.println("Error updating appointment:"+e.getMessage());
        updated = false;
    }
        return updated;
    }

    /**
     * This Method deletes an appointment from the MySQL database if it matches the Appointment_ID.
     *
     * @param appointment the appointment object being deleted.
     * @return true if successfully deleted. False if not.
     */
    public static boolean delete(Appointment appointment){

        String sql= "DELETE FROM appointments WHERE Appointment_ID = ?";
        boolean deleted = false;

        try (PreparedStatement deleteSqlStatement = DBConnection.getConnection().prepareStatement(sql)) {
            deleteSqlStatement.setInt(1, appointment.getAppointmentID());
            deleteSqlStatement.executeUpdate();
            deleted = true;
        } catch (SQLException e) {
            System.err.println("Error deleting appointment:"+e.getMessage());
        }
        return deleted;
    }

