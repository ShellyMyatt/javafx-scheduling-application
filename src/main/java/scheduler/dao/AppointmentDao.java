    /**
     * Retrieves all appointments by type from the database.
     *
     * @return an ObservableList containing appointment types and totals.
     */
    public static ObservableList<Appointment> getAppointmentsByType() {
        ObservableList<Appointment> appointmentsByType = FXCollections.observableArrayList();
        String sql = "SELECT Type, COUNT(*) as Total From appointments GROUP BY Type";

        try (PreparedStatement statement = DBConnection.getConnection().prepareStatement(sql)) {
            ResultSet resultSet = statement.executeQuery();
            while (resultSet.next()) {
                String type = resultSet.getString("Type");
                int total = resultSet.getInt("Total");
                appointmentsByType.add(new Appointment(total,type));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return appointmentsByType;
    }

    /**
     * Retrieves all appointments by month from the database.
     *
     * @return an ObservableList containing the total number of appointments per month.
     */
    public static ObservableList<Reports> getAppointmentsByMonth() {
        ObservableList<Reports> appointmentsByMonth = FXCollections.observableArrayList();
        String sql = "SELECT MONTH(Start) as Month, COUNT(*) as Total From appointments GROUP BY Month(Start)";

        try (PreparedStatement statement = DBConnection.getConnection().prepareStatement(sql)) {
            ResultSet resultSet = statement.executeQuery();
            while (resultSet.next()) {
                int monthNumber = resultSet.getInt("Month");
                String reportMonth = Month.of(monthNumber).name();
                int total = resultSet.getInt("Total");

                appointmentsByMonth.add(new Reports(total, reportMonth));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return appointmentsByMonth;
    }

